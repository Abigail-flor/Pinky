#Ej.Comparaciones

x = float(input("Ingrese un número cualquiera: "))
y = float(input("Ingrese un segundo número: "))

comp1 = x > y
comp2 = x < y
comp3 = x == y
comp4 = x != y
comp5 = x >= y
comp6 = x <= y

print(f"x > y: {comp1}")
print(f"x < y: {comp2}")
print(f"x == y: {comp3}")
print(f"x != y: {comp4}")
print(f"x >= y: {comp5}")
print(f"x <= y: {comp6}")